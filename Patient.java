public class Patient{

        String name;
        String email;
        String address;
        String phoneNumber;
        double weight;
        double height;

        Patient(String name, String email, String address, String phoneNumber, double weight, double height) {
                this.name = name;
                this.email = email;
                this.address = address;
                this.phoneNumber = phoneNumber;
                this.weight = weight;
                this.height = height;
        }
}
